* das g nur wenn vorher ein doppelter Vokal oder ein ch oder ein chr steht.<br/>

Endet ein Wort dabei ohne Endung schon auf einen einfachen Vokal und beginnt die Endung mit einem einfachen Vokal, so wird der die Grundform beendende Vokal fortgelassen.
Endet ein Wort nicht auf einem Konsonanten aber nicht auf einem ch oder chr, so wird dieser Konsonant oft von seiner Silbe gelöst und in die Endung reingezogen, sofern die Endung mit ihm kombiniert eine gültige Silbe ist.
<div class="iiooqas-example">
vich; vichi|gehen; ich gehe
ritlinasfva; ritlinasfvi|überlegen; ich überlege
</div>
Außerdem gibt es noch die Aktivgrundform durch weglassen des letzten Buchstabens und die Passivgrundform durch Ersetzen des letzten Buchstabens durch p.<br/>
Die Vergangenheitsgrundformen werden durch Ersetzen des letzten Buchstabens durch e im Aktiv und ep im Passiv gebildet.<br/>
Ausnahmen: Die Vergangenheit von achilla ist bolisalt mit Präsensformen. salutis wird im deutschen mit gekocht werden übersetzt, ist im iiooqischen aber im Aktiv.
<div class="iiooqas-example">
achilli; bolisalti|ich laufe; ich lief
achill; achillp|laufen (Grundform); gelaufen werden (Grundform)
</div>

Spekulation über Aktionen in der Zukunft werden mit Vermutungsfutur ausgedrückt. Dieses besteht aus der Vorsilbe kra- und den Präsensendungen. Falls das Verb schon mit a, aber nicht mit aa beginnt, so ist die Vorsilbe nur kr-. 
<div class="iiooqas-example">
lajat; lajati; kralajati|stehen (Grundform); ich stehe; ich werde vermutlich stehen
achilli; krachilli|laufen; ich werde vermutlich laufen
</div>
<br/>

Verbeigenschaften werden nach dem Verb mit ra eingeleitet.
Ausnahme: Eigenschaften von or werden nach sor direkt eingeleitet, ra entfällt.
<div class="iiooqas-example">
lajati ra téfgkchréégkt.|Ich stehe schlecht.
sori geft.|Ich bin schön.
</div>
<br/>
Mehrere Verben können zu einem mit e kombiniert werden. ra bezieht sich dabei auf jedes einzelne verb, ein folgendes rä auf alle.
<div class="iiooqas-example">
vichi e lajati.|Ich gehe und stehe.
vichi e lajati ra téfgkchréégkt.|Ich gehe und stehe schlecht.
vichi e lajati rä téfgkchréégkt.|Ich gehe schlecht und stehe schlecht.
</div>
<br/>
Verben können außerdem auf vier Arten kombiniert werden:
<ul>
	<li>
Verb + Aktivgrundform (für z.B. müssen, dürfen, wollen)
	<li>
Verb + Passivgrundform
	<li>
Verb + Aktivgrundform (Vergangenheit)
	<li>
Verb + Passivgrundform (Vergangenheit)
</ul>
<div class="iiooqas-example">
ulii achill|ich will laufen
ulii achillp|ich will gelaufen worden sein
ulii engqichriie|ich will gegessen gewesen sein
ulii engqichriiep|ich will gegessen gewesen worden sein
</div>
<br/>
Negation wird vorangestellt. Hierbei wird ni verwendet, außer das Verb beginnt mit einem Vokal, dann wird nil verwendet.<!-- so seit 2024-05-10, davor stand da nur non -->
<div class="iiooqas-example">
vichi.|ich gehe.
ni vichi.|ich gehe nicht.
ora.|Es ist.
nil ora.|Es ist nicht.
</div>
<br/>
Befehlsformen werden durch die Endung -im im Aktiv bzw. -imp im Passiv gebildet. Endet das Verb auf einen einfachen Vokal, so wird dieser vor dem Anhängen der Endung entfernt.
<div class="iiooqas-example">
vich; vichim|gehen (Grundform); geh!
achill; achillimp|laufen (Grundform); werde gelaufen!
</div>
Umgangssprachlich kann die Endung weggelassen werden, wenn die Betonung die Bedeutung schon naheliegt.