<h2>Nomen</h2>

Jedes Wort im Wörterbuch, das eine Nomenübersetzung hat ist an sich die Grundform als Nomen, wobei Grundform im iiooqischen Nominativ Singular bedeutet.
<br/>
Ergänzen der Endung é führt das Wort in den Nominativ Plural über. Wenn das Wort auf einen Vokal, aber nicht Doppelvokal (aa,ee,ii,oo,uu,éé) endet, wird dieser durch das é ersetzt und das é nicht angehangen.
Sollte das Wort vor dem Anfügen der Endung schon auf é enden, so wird das é durch eé ersetzt. Bei Phrasen mit mehreren Wortbestandteilen wird der Plural auf das erste Wort angewandt.
<div class="iiooqas-example">
sekturcht; sekturchté|Sonne; Sonnen
bakro; bakré|Insekt; Insekten
italnii; italniié|Temperatur; Temperaturen
rité; riteé|Tag; Tage
</div>
Beim Plural mit einer bestimmten Anzahl folgt die Zahl direkt dem é. Selbst wenn nur ein oder kein Objekt ausgedrückt werden soll wird hierbei die Pluralform verwendet.
Die anzahlbestimmte Plural wird ebenfalls verwendet, um eine Zahl mit einer Einheit zu kombinieren.
<div class="iiooqas-example">
sekturcht; sekturchté|Sonne; Sonnen
tre; sekturchtétre|drei; drei Sonnen
egs; sekturchtéegs|eins; eine Sonne
riteé; riteétre|Tage; drei Tage
</div><br/>
Genitiv wird durch Voranschreiben des genitiveinleitenden Wortes re gebildet.
<div class="iiooqas-example">
sekturcht; re sekturcht|Sonne; der Sonne
sekturchté; re sekturchté|Sonnen; der Sonnen
</div>
Dativ wird durch Voranschreiben des dativeinleitenden Wortes es gebildet.
<div class="iiooqas-example">
sekturcht; es sekturcht|Sonne; der Sonne
sekturchté; es sekturchté|Sonnen; den Sonnen
</div>
Der Akkusativ wird durch Anhängen der Endung s gebildet. Sollte das Wort auf ch oder ss enden, so wird stattdessen ss durch es ersetzt und bei ch es angehangen.
Das e von es ersetzt außerdem den voherfolgen einfachen Vokal wie das é bei der Pluralendung.
Der Akkusativ Plural bekommt dabei erst die Endung é und dann s.
<div class="iiooqas-example">
sekturcht; sekturchts|Sonne; die Sonne (Akk.)
sekturchté; sekturchtés|Sonnen; die Sonnen (Akk.)
stritumnass; stritumnes|Ameise; die Ameise (Akk.)
stritumnassé; stritumnassés|Ameisen; die Ameisen (Akk.)
</div>
Nach einleitenden Worten wie den folgenden stehen Nomen in der Grundform oder Grundpluralform.<br/>
ak ik ek ok uk fultos lo za …<br/>
<br/>
Sollte ein Nomen Eigenschaften besitzen, werden diese durch Wort i Eigenschaften beschrieben. Siehe das Kapitel zu Adjektiven.
<br/>
Nomen können mit e (übersetzt mit und) dazwischen aufgezählt werden.
Soll sich dabei eine Anzahl auf alle Wörter der Aufzählung statt nur dem direkt folgenden beziehen,
so wird danach jorx (insgesamt) oder rijsa (jeweils) geschrieben.<br/>
Bei Verwendung von jorx kann auf die Anzahl der einzelnen Dinge kein Rückschluss genommen werden, außer sie sind im Singular, dann ist es genau eines. Wenn sie im Plural stehen bedeutet es hier nur "mindestens eines und es ist nicht bekannt dass es genau eines ist", nicht mehr als eines.<br/>
rijsa ist äquivalent dazu, die Anzahl hinter jedes einzelne Wort zu schreiben.
<div class="iiooqas-example">
hechté e gojkétre|Schweine und 3 Gänse
hechté e gojkétre jorx|insgesamt 3 Schweine und Gänse
hechté e gojkétre rijsa|jeweils 3 Schweine und Gänse
hechtétre e gojkétre rijsa|3 Schweine und 3 Gänse
</div>
Stimmt das Subjekt-Nomen mit dem Verb im kleinsten umfassenden Satz überein, wird es durch silat ersetzt. Die Formen von silat werden wie oben beschrieben gebildet.
<div class="iiooqas-example">
afdilitisa silat.|Der Regen regnet.
</div>