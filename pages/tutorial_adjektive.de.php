<h2>Adjektive</h2>

Die Grundform von Adjektiven ist die im Wörterbuch angegebene Form.<br/><br/>
Adjektive können gesteigert werden.<br/>
Quantitativ: -ut -u -ot zur immer weiteren Steigerung der Quantität, -it zur Verminderung derselben<br/>
Qualitativ: -ast zur Steigerung der Qualität, -ist zur Verminderung<br/>
Das Abhandensein einer Eigenschaft geschieht durch Ausdruck des Besitzens der Verneinung derselben.
<div class="iiooqas-example">
lag|groß
lagut|größer
lagu|noch größer
lagot|größtes
lagit|...
</div>
<br/>
Mit diesen Endungen können selbst Verben in ihrer Intensität "gesteigert" werden, indem das verbdetailangebende Wort ra gesteigert wird.
<div class="iiooqas-example">
ployastarv raut|du gefällst sehr (häufig)
</div>

Durch Hinzufügen der Vorsilbe des ist die Vollendung der Eigenschaft ausgedrückt. Diese Formen werden des-Formen genannt.
<div class="iiooqas-example">
äfdtrapfnis; desäfdtrapfnis|antreibend; angetrieben
</div>

Adjektive sind durch é kombinierbar.<br/>

Adjektive sind auf Nomen mit i anwendbar.
Außerdem gibt es den Ausdruck des Erfüllens einer Eigenschaft: a Eigenschaft ä Wert<br/>
Solche Eigenschaften sind mit é als ein Ausdruck weiter kombinierbar und außerdem durch darauf folgende i Eigenschaften kombinierbar
<div class="iiooqas-example">
nak i lagut|größere Ente
nak a qol ä gr|grünfarbige Ente
nak a qol ä gr i lag|grünfarbige große Ente
</div>
<br/>
Eigenschaften sind außerdem mittels ra auf Verben anwendbar.
<div class="iiooqas-example">
vichi ra nilmm uk nak.|Ich gehe schnell zur Ente.
</div>