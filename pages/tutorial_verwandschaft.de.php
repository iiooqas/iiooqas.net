<h2>Verwandschaft</h2>

Direkte Vorfahren werden nach Geschlecht und Generation gruppiert. Ist eine Person auf mehrer Arten und Weisen direkter Vorfahr in unterschiedlichem Grad, so wird meist der kleinere Grad verwendet.
<div class="iiooqas-example">
mitur|Mutter
miret|Großmutter
mitre|Urgroßmutter
mines|Altmutter
fustra|Vater
fusret|Großvater
fustre|Urgroßvater
fusnes|Altvater
fumipa|Elternteil
fumipé|Eltern
furet|Großelternteil
fureté|Großeltern
luratasé|Direkte Vorfahren
</div>
<br/>
Weitere Kinder direkter Vorfahren werden nach Geschlecht und Generation gruppiert.
<div class="iiooqas-example">
misulfalet|Tante
misulfatre|Großtante
yotrilet|Onkel
yotriltre|Großonkel
</div>
<br/>
Weitere Partner direkter Vorfahren und alle weiteren Personen die nur über eine weitere Heirat eines direkten Verwandten angebunden sind erhalten das Nachwort "selif".
Die Kinder eines direkten Vorfahrens mit einem weiteren Partner erhalten den Zusatz "ak egs", wörtlich "ausgehend von einem".
Angeheiratete Partner von Onkel und Tanten beliebiger Generation heißen geschlechtsunabhängig.
<div class="iiooqas-example">
fusret selif|Stiefgroßvater
yotrilet ak egs|Halbonkel
upisodalet|Tante (Partner verwandten Onkels), Onkel (Partner verwandter Tante)
</div>
<br/>
Seltener werden außerdem die Eltern der mitur selif als miret selif und fusret selif bezeichnet. Um diese dann von weiteren Partnern der leiblichen miret oder fusret zu unterscheiden, kann der Titel der Person angegeben werden, von der sich der selif-Zweig abspaltet. Hier also fusret selif miret für Partner der miret gegenüber fusret selif fustra für Eltern der mitur selif.
<br/><br/>
Nun zu Partnern von mir selbst.
<div class="iiooqas-example">
bokri|(Ehe)Partner
fustra rokri|Schwiegervater, Schwäher
mitur rokri|Schwiegermutter, Schwäherin
fumipé rokri|Schwiegereltern
fumipa rokri|Schwiegerelternteil
fureté rokri|Schwiegergroßeltern
qabsetril|Weitere (hoffentlich frühere) Partner meines Partners
</div>
<br/>
Nun zu Kindern.
<div class="iiooqas-example">
neklo|Kind von mir mit meiner Partnerperson
neklo qabsetril|Kind meines Partners mit einem früheren Partner
slitranurpi|Schwiegerkind (Partner meiner Kinder)
sitlufatbis|Gegenschwieger
slitranurpi qabsetril|Partner von Kindern meines Partners mit einem früheren Partner
neklet|Enkelkind, Enkel, Enkelin, Enkelsohn, Enkeltochter, Großsohn, Großtochter
geterb|Bruder
gitrubp|Schwester
afdulipa|Geschwisterteil
afdulipé|Geschwister
geterb rokri|Schwager
gitrubp rokri|Schwägerin
geterb ak egs|Halbbruder
geterb ak egs rokri|Halbschwager
mitur rokri selif|Stiefschwiegermutter
neklo ak geterb rokri|Neffe/Nichte
betako|Cousine, Cousin
</div>
Die Reihenfolge der Zusätze ist: ag egs, rokri, selif, qabsetril. Zu beachten ist immer zuerst qabsetril.
<br/><br/>
Nun zu entfernteren Verwandten.
<div class="iiooqas-example">
mines|Altmutter
misufanes|Urgroßtante
yotriltre ikra 2|Großonkel 2. Grades
yotrilet ikra 3|Onkel 3. Grades
misufanes ak egs|Halb-Urgroßtante (Kind von fusnes und mines selif)
yotriltre ak egs ikra 2|Halb-Großonkel 2. Grades
</div>
Dabei ist ein yotrilet immer dieselbe Generation, der Grad gibt an, wie viele Generationen der direkte Vorfahr entfernt ist.
<br/><br/>
Kuriositäten:
<div class="iiooqas-example">
upsidatre ak egs rokri qabsetril ikra 2|Angeheiratete Partnerperson eines/einer Halbgroßonkels/Halbgroßtante 2. Grades eines Partners meines Partners
afdulipa ak lunatur|Bezeichnung für Personen mit dem gleichen Geburtstag (nicht unbedingt verwandt)
</div>
