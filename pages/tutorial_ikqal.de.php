<h2>Sonstiges</h2>

Abkürzungen bestehen meist aus dem ersten und letzten Buchstaben oder den ersten und letzten beiden. Es wird vor und nach die Abkürzung ein Abkürzungszeichen, hier durch einen Punkt dargestellt, geschrieben.
<div class="iiooqas-example">
endagql; .el.|Geburt; Geburt
xentorustybusagerapubik; .xeik.|Videospiel; Videospiel
</div>

tanoch steht immer direkt nach dem Verb
<br/>
Wenn schon behandelte Objekte erneut referenziert werden sollen, kann dies mit dem Wort der direkten Objektreferenz geschehen. Dieses referenziert immer das zuletzt direkt genannte Objekt.
ikarv steht im Plural als ikarvé.
<div class="iiooqas-example">
vicha gojk.|Die Gans geht.
vichi uk nak.|Ich gehe zur Ente.
vicha nak. vicha ikarv uk nisvilguré. rastura ikarv.|Die Ente geht. Sie geht zum Wald. Dieser brennt.
</div>
Im letzten Satz bezieht sich ikarv erst auf die Ente und dann auf den Wald. Mehrere Objekte können mit Und verknüpft werden. Objektpräpositionen beziehen sich dabei auf den ganzen Und-Ausdruck.
<div class="iiooqas-example">
vichi uk gojk e nak.|Ich gehe zu Gans und Ente.
</div>
