Sätze:
Wird der feste Anfangsort beschrieben oder die Objekte nach ihrem Ort unterschieden, wird dies nach dem Nomen nach i beschrieben, wenn dies durch eine simple absolute Beschreibung möglich ist. Falls nicht, können Detailsätze verwendet werden.<br/>
<div class="iiooqas-example">
lajata nak i ruil.|Die obere Ente steht.
lajata nak ok hecht.|Die Ente steht neben dem Schwein.
lajata nak de ok hecht.|Die sich neben dem Schwein befindliche Ente steht.
vichi de ok hecht.|Ich gehe während ich mich bei dem Schwein befinde.
</div>
Wird der Ablauf einer Bewegung relativ zu etwas absoluten beschrieben, wird die Relation mit ak, ik, ... beschrieben.<br/>
<div class="iiooqas-example">
vichi uk riul.|Ich gehe nach oben.
vichi uk siléénchi.|Ich gehe zur Wiese hin.
vicha nak ok hecht i inzartéll.|Die Ente geht neben dem sich unten links befindenden Schwein.
</div>
Verläuft der Ablauf des Verbs in eine bestimmte Richtung, so wird dies nach dem Verb mit po beschrieben.<br/>
<div class="iiooqas-example">
vicha po polagrunoo hecht.|Das Schwein geht von oben links nach unten rechts.
</div>
Wird zwischen verschiedenen Bewegungen mit der Richtung unterschieden, so wird dies nach dem Verb mit ra beschrieben.
<div class="iiooqas-example">
vichi ra ritolarg.|Ich gehe seitlich.
</div>