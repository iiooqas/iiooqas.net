<h2>Zahlen</h2>

Zahlen werden hexadezimal angegeben. Es gibt jeweils kleine und große Zahlen.
Von 0 bis 15 gibt es ein eigenes Wort pro natürlicher Zahl. Hierbei wird nicht zwischen groß und klein unterschieden.
Die vielfachen von 16 werden mit der Endung -as angegeben.
<div class="iiooqas-example">
cham|5
chamas|80 (=5*16)
qarp|15
qarpascham|245 (=15*16+5)
[Zeichen für kleine 15][Zeichen für kleine 5]|245
</div>

Für die Groß/Kleinschreibung gelten folgende Regeln:
Nur jede vierte Ziffer vor dem Komma wird groß geschrieben. Nach dem Komma wird jede Ziffer außer jeder vierten groß geschrieben.
Wochen und Wochentage werden immer groß geschrieben. Tage werden immer klein geschrieben. Die ersten 2 Ziffern vom Jahr werden groß geschrieben, die letzte klein.
silut und tar werden großgeschrieben, hexek und zis klein.

Das Jahr besteht aus 21 Wochen zu je 16 Tagen.
Die Wochen und Wochentage haben jeweils Namen.

Die Wochennamen geben außerdem ein symbolisches Thema für die Woche vor. Direkt übersetzt bedeuten sie folgendes:
<div class="iiooqas-example">
mnitisza, drukotvii, nisvilgur, prutra|Anfang, Licht, Baum, Bedingung
vfertééln, buchralof, rifintaluurg, ralx|Versuch, Gemälde, Wölbung, Messer
riteé, boviteer, ganati, randtal|Tage, Flucht, Unwetter, Mitte
dalqasé, niloqas, zibyqas, akel|Sprüche, Nebel, Knoten, Tier
volnir, trangqa, gantalit, trusa|Erschaffen, Historie, Tonne, Liebe
vadlas, bach, entulitra|Tausch, Geist, Ende
nivis mnitisza|Die erste Woche im Jahr
</div>

Jahre werden ab -663.5 vor Christus gezählt und beginnen am 28. Juni. a7f ist also das Jahr 2023/24.