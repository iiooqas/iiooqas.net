Ausnahmen/Weirdheiten:<br/>
é und e und ä ist manchmal inconsistent, da e mit punkt entfernt wurde. Vor verdoppelten Konsonanten wird e wie é oder ä ausgesprochen.<br/>
y ist entweder j, ji oder ü. In jy ist es immer ü, am Wortanfang j. ts hat Ähnlichkeit zm deutschen tz, tz zum deutschen tss.
<br/><br/>
h ist dabei nur am Wortanfang erlaubt. é ist überall außer am Wortanfang erlaubt (das betrifft aber nicht éé). gq ist nicht am Wortanfang erlaubt.

<br/><br/>
Im Wörterbuch steht immer nur ein iioo Wort, aus welchem die Formen für Nomen, Verb, Adjektiv hergeleitet werden.<br/><br/>

Betonung:<br/>
Wenn ein doppelter Vokal oder éé existiert auf diesem<br/>
Ansonsten kommt es aufs Wort an. Falls drei oder mehr Silben existieren ist nur sehr selten die letzte betont.