<h2>Phonotactics</h2>
CCCCVCCCCC, aber die Konsonantencluster sind sehr limitiert.<br/>

97% der Silben sind nur CCVCC.<br/>

Cluster mit mehr als 3 Konsonanten entstehen aus solchen mit 3en durch<br/>
- Ersetzen von m oder n durch mn oder nm<br/>
- Ersetzen von g durch gr<br/>
- Einfügen von k nach einem endenden b, d oder p<br/>
- Ersetzen von p durch pf

<h2>Betonung</h2>
Wenn ein doppelter Vokal oder éé in einer Silbe existiert, auf dieser.
Ansonsten je nach dem.

